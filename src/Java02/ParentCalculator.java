package Java02;

public class ParentCalculator {

    public int AddOperation(int x, int y)
    {
        return x+y;
    }

    public int SubtractionOperation(int x, int y)
    {
        return x-y;
    }
    public int MultiplicationOperation(int x, int y)
    {
        return x*y;
    }
    public int DivisionOperation(int x, int y)
    {
        return x/y;
    }
}
